function findTopic(text){
  const t = text.toLowerCase();
  for (const key in TOPIC_LIBRARY){
    if (t.includes(key)) return TOPIC_LIBRARY[key];
  }
  return null;
}

/* ---------- Ответ чата ---------- */
function generateChatReply(subjectId, userText){
  const meta = SUBJECTS_META[subjectId] || { name:'выбранному предмету' };
  const taskMatch = userText.match(/задач[аи]?\s*№?\s*(\d+)/i);
  const topic = findTopic(userText);

  if (taskMatch){
    return {
      html: renderSteps({
        title: `Разбор задачи №${taskMatch[1]}`,
        intro: `Отлично, разберём задачу №${taskMatch[1]} по предмету «${meta.name}» пошагово.`,
        steps: genericSteps(userText),
        tip: 'Если пришлёте полный текст условия в чат или в раздел «Решение задач», разбор будет точнее — с числами именно из вашей задачи.'
      }),
    };
  }

  if (topic){
    return { html: renderTopic(topic) };
  }

  if (/план/i.test(userText)){
    return { html: renderTopic(TOPIC_LIBRARY['план']) };
  }

  // generic helpful fallback
  return {
    html: renderTopic({
      title: `По теме «${userText.trim().slice(0,60)}»`,
      body: [
        `Разберём это по предмету «${meta.name}». Сначала — короткая теория, затем пример, а в конце — задание для закрепления.`,
        'Если нужно, уточните формулировку или пришлите номер задания из учебника/сборника — сделаю разбор точнее.'
      ],
      example: 'Пример разберём на основе типовой задачи этого раздела, как только уточните детали.',
      practice: 'Попробуйте сформулировать вопрос ещё конкретнее, например: «Объясни правило на 2 примерах» или «Реши задачу №12 из варианта».'
    })
  };
}

