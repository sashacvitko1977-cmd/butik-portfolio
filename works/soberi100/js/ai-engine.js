function generatePlan({ subjectId, grade, targetScore, hoursWeek }){
  const topics = PLAN_TOPICS[subjectId] || PLAN_TOPICS.math;
  const weeksCount = Math.min(10, Math.max(4, Math.round(hoursWeek >= 5 ? topics.length * 0.7 : topics.length)));
  const perWeek = Math.ceil(topics.length / weeksCount);

  const weeks = [];
  for (let w = 0; w < weeksCount; w++){
    const slice = topics.slice(w*perWeek, w*perWeek + perWeek);
    if (slice.length === 0) break;
    weeks.push({
      number: w + 1,
      topics: slice,
      hours: hoursWeek,
    });
  }
  return {
    subjectId,
    grade,
    targetScore,
    hoursWeek,
    weeks,
    createdAt: new Date().toISOString(),
  };
}
