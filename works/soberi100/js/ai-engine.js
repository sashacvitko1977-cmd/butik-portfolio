/* ---------- Рендер топика в HTML ---------- */
function renderTopic(topic){
  let html = `<h4>${escapeHtml(topic.title)}</h4>`;
  (topic.body||[]).forEach(p => html += `<p>${escapeHtml(p)}</p>`);
  if (topic.example){
    html += `<div class="ai-block ai-example"><span class="ai-block-label">Пример</span><pre>${escapeHtml(topic.example)}</pre></div>`;
  }
  if (topic.practice){
    html += `<div class="ai-block ai-practice"><span class="ai-block-label">Закрепим</span><p>${escapeHtml(topic.practice)}</p></div>`;
  }
  if (topic.linkTo){
    html += `<a href="${topic.linkTo.href}" class="btn btn-primary btn-sm" style="margin-top:8px;">${escapeHtml(topic.linkTo.label)}</a>`;
  }
  return html;
}

function renderSteps({title, intro, steps, tip}){
  let html = `<h4>${escapeHtml(title)}</h4><p>${escapeHtml(intro)}</p><ol class="ai-steps">`;
  steps.forEach(s => html += `<li>${escapeHtml(s)}</li>`);
  html += `</ol>`;
  if (tip) html += `<div class="ai-block ai-practice"><span class="ai-block-label">Совет</span><p>${escapeHtml(tip)}</p></div>`;
  return html;
}

function escapeHtml(str){
  const div = document.createElement('div');
  div.textContent = str;
  return div.innerHTML;
}

/* ---------- Решение задачи (раздел «Решение задач») ---------- */
function generateProblemSolution(subjectId, problemText){
  const topic = findTopic(problemText);
  const meta = SUBJECTS_META[subjectId] || { name:'предмету' };

  const steps = [];
  steps.push('Читаем условие и выделяем данные: что известно и что нужно найти.');

  if (topic){
    steps.push(`Определяем тему: «${topic.title}». ${topic.body[0]}`);
    if (topic.body[1]) steps.push(topic.body[1]);
    if (topic.example) steps.push(`Применяем на похожем примере: ${topic.example}`);
  } else {
    steps.push(`Подбираем метод решения, характерный для раздела «${meta.name}» — вспоминаем формулы и правила по теме условия.`);
    steps.push('Выполняем вычисления или преобразования по шагам, не пропуская промежуточные действия.');
  }

  steps.push('Проверяем, что результат отвечает на вопрос задачи, и правдоподобен по смыслу (знак, порядок величины, единицы измерения).');
  steps.push('Записываем итоговый ответ в требуемом формате (число, интервал, слово — как указано в задании).');

  return steps;
}

